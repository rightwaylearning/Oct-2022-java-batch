package vaishnavi.shitole;

public class Pattern_2 {

	public static void main(String[] args) {

		for (int j = 5; j >= 1; j--) {

			for (int i = 1; i <= j; i++) {
				System.out.print("* ");

			}
			System.out.println();
		}

		System.out.println("-------------------------");

		for (int j = 5; j >= 1; j--) {

			for (int i = 1; i <= j; i++) {
				System.out.print("A ");

			}
			System.out.println();
		}

	}
}
