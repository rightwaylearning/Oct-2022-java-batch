package sarthak_nalage;

public class PrimtiveRange {
	public static void main(String[] args) {
		char A='A';
		
		for(int i=0;i<26;i++) {
			int a=(int)A;
			System.out.println(A+" "+a);
			A++;
		}
		System.out.println("Printing a to z");
		char a='a';
		for(int i1=0;i1<26;i1++) {
			int a1=(int)a;
			System.out.println(a+" "+a1);
			a++;
		}
		
		
	}

}
