package Rutuja.Nimbhore;

public class Student6 {
	private int rollNumber;
	private String name;
    private String std;
    
    Student6()
    {
    	
    }
    
    Student6(int rollNumber, String name, String std) {
		
		this.rollNumber = rollNumber;
		this.name = name;
		this.std = std;
	}
    
	 int getRollNumber() {
		return rollNumber;
	}
	 void setRollNumber(int rollNumber) {
		this.rollNumber = rollNumber;
	}
	 String getName() {
		return name;
	}
	 void setName(String name) {
		this.name = name;
	}
	 String getStd() {
		return std;
	}
	 void setStd(String std) {
		this.std = std;
	}

	
    
}
