package varsha_tavhare;

public class Stud {
	
	
	private int rno;
	private String snm;
	private String std;
	
	public Stud(int rno, String snm, String std) {
		super();
		this.rno = rno;
		this.snm = snm;
		this.std = std;
	}
	
	public int getRno() {
		return rno;
	}
	public void setRno(int rno) {
		this.rno = rno;
	}
	public String getSnm() {
		return snm;
	}
	public void setSnm(String snm) {
		this.snm = snm;
	}
	public String getStd() {
		return std;
	}
	public void setStd(String std) {
		this.std = std;
	}

	
}
