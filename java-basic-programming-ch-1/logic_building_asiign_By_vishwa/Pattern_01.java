public class Pattern_01 {

	public static void main(String[] args) {

		for (int v = 1; v <= 5; v++) {

			for (int r = 1; r <=v; r++) {

				System.out.print("* ");
			}

			System.out.println();

		}

		
		char ch = 'A';
		for (int v = 1; v <= 5; v++) {

			for (int r = 1; r <= v; r++) {
				System.out.print(ch + " ");
			}

			System.out.println();
			ch++;
		}

		System.out.println("-----------------------------------");
		
		
		
		
		
		
		
		
	}
}












