package yogesh.kawade;

public class Pattaern_4 {
	 
	
	void printstar(int n) {
		
		for(int i=1; i<=n; i++) {
			System.out.print("* ");
			
		}
		System.out.println();
	}

	public static void main(String[]args) {
		
		Pattaern_4 p = new Pattaern_4();
		p.printstar(1);
		p.printstar(2);
		p.printstar(3);
		p.printstar(4);
		p.printstar(5);
		
		
	}
	
}	
		
	

	
	
	
	