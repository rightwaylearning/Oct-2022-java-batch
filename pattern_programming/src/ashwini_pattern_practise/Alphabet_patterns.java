package ashwini_pattern_practise;

public class Alphabet_patterns {
	public static void main(String[] args) {
		char ch='A';
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print(ch + " ");
				
			}
			System.out.println();
		}
		
		
		System.out.println(">>>>>>>>>...............>>>>>>>>>>>>>>>>>");
		
		
	}

}
