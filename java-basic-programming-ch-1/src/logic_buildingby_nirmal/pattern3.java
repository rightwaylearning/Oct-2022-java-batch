package logic_buildingby_nirmal;

public class pattern3 {

	public static void main(String[] args) {
		
		
		for(int e = 1; e < 10; e--) {
			
			for(int f = 1; f < e; f++) {
				
				System.out.print("* ");
		}
			System.out.println();
			
			
	}
		char ch = 'A';
		for(int e = 1; e < 10; e--) {
			
			for(int f = 1; f < e; f++) {
				
				System.out.print(ch +" ");
		}
			System.out.println();
			ch++;
			
	}
	}
}