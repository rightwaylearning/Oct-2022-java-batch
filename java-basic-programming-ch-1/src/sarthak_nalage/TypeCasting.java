package sarthak_nalage;

public class TypeCasting {
	public static void main(String[] args) {
		long l=3467354653764L;
		int i=(int) l;
		System.out.println(i);//ans 1316045892
		float f=45.34F;
		int i1=(int)f;
		System.out.println(i1);//ans 45
		char c='a';
		int i2=c;
		System.out.println(i2);
		long l1=2343456562323L;
		float f2=l1;
		System.out.println(f2);
		long l2=(long)f2;
		System.out.println(l2);
	}
}
