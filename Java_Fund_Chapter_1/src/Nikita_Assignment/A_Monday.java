package Nikita_Assignment;

public class A_Monday {
	private int x;
	private int y;
	
	A_Monday(){}
	
	A_Monday (int x , int y){
		
		this.x = x;
		this.y = y;
		
		
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	

}
