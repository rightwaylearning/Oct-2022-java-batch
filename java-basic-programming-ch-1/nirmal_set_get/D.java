package nirmal_set_get;

public class D {

	private int a;
	private int b;

	D() {
	}

	D(int c, int d) {
		this.a = c;
		this.b = d;
		
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}
}


